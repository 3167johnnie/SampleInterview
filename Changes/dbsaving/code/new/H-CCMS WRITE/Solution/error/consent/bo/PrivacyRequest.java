public class PrivacyRequest {
	
	String auth_Key;
	String request;

    
	
	public String getAuth_Key() {
		return auth_Key;
	}

	public void setAuth_Key(String auth_Key) {
		this.auth_Key = auth_Key;
	}

	public String getRequest() {
		return request;
	}

	public void setRequest(String request) {
		this.request = request;
	}
	
	
}
